public class MathFunctions {
    public static void main(String[] args) {
        System.out.println("Math.abs(-26.7) = " + Math.abs(-26.7));
        System.out.println("Math.ceil(7.4) = " + Math.ceil(7.4));
        System.out.println("Math.floor(7.8) = " + Math.floor(7.8));
        System.out.println("Math.max(8.6, 5.2) = " + Math.max(8.6, 5.2));
        System.out.println("Math.min(8.6, 5.2) = " + Math.min(8.6, 5.2));
        System.out.println("Math.pow(2, 3) = " + Math.pow(2, 3));
        System.out.println("Math.sqrt(9) = " + Math.sqrt(9));
    }
}
